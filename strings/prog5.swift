//program to demonstrate Strings
import Foundation
var name : String = "welcome"
var str = String("ios trainer")
//capitalized property
print(name.capitalized)
print(str.capitalized)
let cp = name.capitalized
print(cp)
let cap = str.capitalized
print(cap)
//uppercased() function
print(name.uppercased())
print(str.uppercased())
let up = name.uppercased()
print(up)
let upc = str.uppercased()
print(upc)
//lowercased() function
print(name.lowercased())
let loc = name.lowercased()
print(loc)