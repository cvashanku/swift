//program to demonstrate round a number to n decimal places by using ceil() function
//grater than or equal to the given number
import Foundation

var n1 : Float = 2.45
var n2 : Double = 42.93
var n3 : Double = -2.03

// Floating point number
print("Round number \(n1):", ceil(n1))

// Positive double number
print("Round number \(n2):", ceil(n2))

// Negative double number
print("Round number \(n3):", ceil(n3))