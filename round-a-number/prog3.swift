//program to demonstrate round a number to n decimal places by using floor() function
//less than or equal to the given number
import Foundation

var n1 : Float = 23.45
var n2 : Double = 4.89
var n3 : Double = -7.09

// Floating point number
print("Round number \(n1):", floor(n1))

// Positive double number
print("Round number \(n2):", floor(n2))

// Negative double number
print("Round number \(n3):", floor(n3))