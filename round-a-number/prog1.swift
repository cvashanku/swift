//program to demonstrate round a number to n decimal places by using round() function
//the nearest whole number
import Foundation
var n1 : Float = 6.32
var n2 : Double = 876.876
var n3 : Double = -0.987

// Floating point number
print("Round number \(n1):", round(n1))

// Positive double number
print("Round number \(n2):", round(n2))

// Negative double number
print("Round number \(n3):", round(n3))